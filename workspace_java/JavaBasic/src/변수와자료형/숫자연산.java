package 변수와자료형;

public class 숫자연산 {
    public static void main(String[] args) {

        System.out.println(10+10);
        //*****************정수끼리의 연산 결과는 정수로 나옴*****
        System.out.println(10/4);
        //실수끼리의 연산 결과는 실수로 나옴
        System.out.println(10.0/4.0);
        //정수와 실수끼리의 결과는 실수로 나옴
        System.out.println(10.0/4);

        System.out.println(10.0/3*1.5);


    }
}
