package 네번째_조건문;

public class If_2 {
    public static void main(String[] args) {
        System.out.println("프로그램 시작!");

        int x = 7;
        //x의 값이 7이면 1을 출력 그렇지 않으면 2 출력
        //만약에... 내가 간다면... 내가 떠나 간.. 다면...
        if(x==7){
            System.out.println("1");

        }
        //그렇지 않으면
        else {
            System.out.println("2");
        }

        System.out.println("프로그램 종료~");
    }
}
